// Add five integers (without using variables) and display their sum.

package Java;

public class ProgramInt {
    
    public static void main(String[] args) 
{
		
        int num1 = 20;
        int num2 = 40;
        int num3 = 30;
        int num4 = 60;
        int num5 = 19;
      
        int sum = num1 + num2 + num3 + num4 + num5;
      
      System.out.println("Sum of Integers using variable is : "+sum);

	}
}
